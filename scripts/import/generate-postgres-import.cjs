#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..', '..');
const defaultSource = path.join(repoRoot, 'db.json');
const defaultOutput = path.join(repoRoot, 'db', 'seeds', 'current_snapshot.generated.sql');

function parseArgs(argv) {
  const args = {
    source: defaultSource,
    output: defaultOutput,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--source' && argv[i + 1]) {
      args.source = path.resolve(argv[i + 1]);
      i += 1;
      continue;
    }
    if (arg === '--output' && argv[i + 1]) {
      args.output = path.resolve(argv[i + 1]);
      i += 1;
      continue;
    }
    if (arg === '--help' || arg === '-h') {
      printHelp();
      process.exit(0);
    }
  }

  return args;
}

function printHelp() {
  console.log(`
Generate PostgreSQL import SQL from the current db.json snapshot.

Usage:
  node scripts/import/generate-postgres-import.cjs [--source <db.json>] [--output <file.sql>]

Defaults:
  --source ${defaultSource}
  --output ${defaultOutput}
`);
}

function sqlText(value) {
  if (value === undefined || value === null) return 'NULL';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlBoolean(value) {
  if (value === undefined || value === null) return 'NULL';
  return value ? 'TRUE' : 'FALSE';
}

function sqlNumber(value) {
  if (value === undefined || value === null || value === '') return 'NULL';
  const numeric = Number(value);
  return Number.isFinite(numeric) ? String(numeric) : 'NULL';
}

function sqlJson(value, fallback) {
  const normalized = value === undefined || value === null ? fallback : value;
  return `${sqlText(JSON.stringify(normalized))}::jsonb`;
}

function sqlTimestamp(value) {
  if (!value) return 'NULL';
  return `${sqlText(value)}::timestamptz`;
}

function sqlDate(value) {
  if (!value) return 'NULL';
  return `${sqlText(value)}::date`;
}

function row(values) {
  return `(${values.join(', ')})`;
}

function writeInsert(lines, table, columns, rows) {
  if (!rows.length) return;
  lines.push(`INSERT INTO ${table}`);
  lines.push(`  (${columns.join(', ')})`);
  lines.push('VALUES');
  lines.push(rows.map((entry) => `  ${entry}`).join(',\n'));
  lines.push(';');
  lines.push('');
}

function buildStatements(db) {
  const lines = [];
  const now = new Date().toISOString();

  lines.push('-- Generated by scripts/import/generate-postgres-import.cjs');
  lines.push(`-- Source snapshot generated at ${now}`);
  lines.push('');
  lines.push('BEGIN;');
  lines.push('');
  lines.push('TRUNCATE TABLE');
  lines.push('  push_records,');
  lines.push('  operation_logs,');
  lines.push('  crawl_jobs,');
  lines.push('  alerts,');
  lines.push('  reports,');
  lines.push('  bid_packages,');
  lines.push('  bid_results,');
  lines.push('  policies,');
  lines.push('  competitor_news,');
  lines.push('  intel_sources,');
  lines.push('  competitors');
  lines.push('RESTART IDENTITY CASCADE;');
  lines.push('');

  writeInsert(
    lines,
    'competitors',
    [
      'id',
      'name',
      'short_name',
      'stock',
      'bu',
      'listing',
      'market_cap',
      'customer_group',
      'product_line',
      'main_products',
      'core_strengths',
      'market_share',
      'recent_action',
      'threat_level',
      'status',
      'metadata',
    ],
    (db.competitors || []).map((item) => row([
      sqlText(item.id),
      sqlText(item.name),
      sqlText(item.shortName),
      sqlText(item.stock),
      sqlJson(item.bu, []),
      sqlBoolean(item.listing),
      sqlText(item.marketCap),
      sqlJson(item.customerGroup, []),
      sqlJson(item.productLine, []),
      sqlJson(item.mainProducts, []),
      sqlJson(item.coreStrengths, []),
      sqlJson(item.marketShare, {}),
      sqlText(item.recentAction),
      sqlText(item.threatLevel),
      sqlText(item.status || 'active'),
      sqlJson({}, {}),
    ])),
  );

  writeInsert(
    lines,
    'competitor_news',
    [
      'id',
      'competitor_id',
      'competitor_name',
      'title',
      'content',
      'summary',
      'tag',
      'tag_label',
      'impact_analysis',
      'source_name',
      'source_url',
      'published_at',
      'sentiment',
      'action_required',
      'pushed_to',
      'status',
      'raw_payload',
    ],
    (db.competitorNews || []).map((item) => row([
      sqlText(item.id),
      sqlText(item.competitorId),
      sqlText(item.competitorName),
      sqlText(item.title),
      sqlText(item.content),
      sqlText(item.summary),
      sqlText(item.tag),
      sqlText(item.tagLabel),
      sqlText(item.impactAnalysis),
      sqlText(item.source),
      sqlText(item.sourceUrl),
      sqlTimestamp(item.publishedAt),
      sqlText(item.sentiment),
      sqlBoolean(item.actionRequired),
      sqlJson(item.pushedTo, []),
      sqlText(item.status || 'published'),
      sqlJson(item, {}),
    ])),
  );

  writeInsert(
    lines,
    'policies',
    [
      'id',
      'title',
      'content',
      'source_name',
      'source_url',
      'published_at',
      'impact_level',
      'affected_products',
      'opportunities',
      'threats',
      'impact_analysis',
      'recommendation',
      'status',
      'raw_payload',
    ],
    (db.policies || []).map((item) => row([
      sqlText(item.id),
      sqlText(item.title),
      sqlText(item.content),
      sqlText(item.source),
      sqlText(item.sourceUrl),
      sqlTimestamp(item.publishedAt),
      sqlText(item.impactLevel),
      sqlJson(item.affectedProducts, []),
      sqlJson(item.opportunities, []),
      sqlJson(item.threats, []),
      sqlText(item.impactAnalysis),
      sqlText(item.recommendation),
      sqlText(item.status || 'active'),
      sqlJson(item, {}),
    ])),
  );

  writeInsert(
    lines,
    'bid_results',
    [
      'id',
      'competitor_id',
      'competitor_name',
      'project_name',
      'project_type',
      'amount',
      'market_share',
      'share_change',
      'main_categories',
      'bid_date',
      'status',
      'raw_payload',
    ],
    (db.bidResults || []).map((item) => row([
      sqlText(item.id),
      sqlText(item.competitorId),
      sqlText(item.competitorName),
      sqlText(item.projectName),
      sqlText(item.projectType),
      sqlNumber(item.amount),
      sqlText(item.marketShare),
      sqlText(item.shareChange),
      sqlJson(item.mainCategories, []),
      sqlDate(item.bidDate),
      sqlText(item.status || 'won'),
      sqlJson(item, {}),
    ])),
  );

  writeInsert(
    lines,
    'bid_packages',
    [
      'id',
      'created_at',
      'project_id',
      'project_name',
      'bid_history',
      'competitor_news',
      'competitors_snapshot',
      'analysis',
      'recommendations',
      'raw_payload',
      'updated_at',
    ],
    (db.bidPackages || []).map((item) => row([
      sqlText(item.id),
      sqlTimestamp(item.createdAt),
      sqlText(item.projectId),
      sqlText(item.projectName),
      sqlJson(item.bidHistory, []),
      sqlJson(item.competitorNews, []),
      sqlJson(item.competitors, []),
      sqlJson(item.analysis, {}),
      sqlJson(item.recommendations, []),
      sqlJson(item, {}),
      sqlTimestamp(item.createdAt || now),
    ])),
  );

  writeInsert(
    lines,
    'reports',
    [
      'id',
      'created_at',
      'report_type',
      'period',
      'summary',
      'details',
      'recommendations',
      'raw_payload',
      'updated_at',
    ],
    (db.reports || []).map((item) => row([
      sqlText(item.id),
      sqlTimestamp(item.createdAt),
      sqlText(item.reportType),
      sqlText(item.period),
      sqlJson(item.summary, {}),
      sqlJson(item.details, {}),
      sqlJson(item.recommendations, []),
      sqlJson(item, {}),
      sqlTimestamp(item.createdAt || now),
    ])),
  );

  writeInsert(
    lines,
    'alerts',
    [
      'id',
      'level',
      'title',
      'content',
      'source_name',
      'competitor_id',
      'created_at',
      'alert_type',
      'raw_payload',
      'updated_at',
    ],
    (db.alerts || []).map((item) => row([
      sqlText(item.id),
      sqlText(item.level),
      sqlText(item.title),
      sqlText(item.content),
      sqlText(item.source),
      sqlText(item.competitorId),
      sqlTimestamp(item.createdAt),
      sqlText(item.type),
      sqlJson(item, {}),
      sqlTimestamp(item.createdAt || now),
    ])),
  );

  const intelSources = [];
  const sourceGroups = db.intelSources || {};
  Object.entries(sourceGroups).forEach(([group, items]) => {
    (items || []).forEach((item) => {
      intelSources.push({ sourceGroup: group, ...item });
    });
  });

  writeInsert(
    lines,
    'intel_sources',
    [
      'id',
      'source_group',
      'name',
      'url',
      'category',
      'customer_group',
      'priority',
      'topics',
      'status',
      'raw_payload',
      'created_at',
      'updated_at',
    ],
    intelSources.map((item) => row([
      sqlText(item.id),
      sqlText(item.sourceGroup),
      sqlText(item.name),
      sqlText(item.url),
      sqlText(item.category),
      sqlText(item.customerGroup),
      sqlText(item.priority),
      sqlJson(item.topics, []),
      sqlText('active'),
      sqlJson(item, {}),
      sqlTimestamp(now),
      sqlTimestamp(now),
    ])),
  );

  lines.push('COMMIT;');
  lines.push('');
  return lines.join('\n');
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const db = JSON.parse(fs.readFileSync(args.source, 'utf8'));
  const sql = buildStatements(db);
  fs.mkdirSync(path.dirname(args.output), { recursive: true });
  fs.writeFileSync(args.output, sql, 'utf8');

  const intelSourceCount = Object.values(db.intelSources || {}).reduce(
    (sum, items) => sum + (items || []).length,
    0,
  );

  console.log(`Generated PostgreSQL import SQL:
  source: ${args.source}
  output: ${args.output}
  competitors: ${(db.competitors || []).length}
  competitorNews: ${(db.competitorNews || []).length}
  policies: ${(db.policies || []).length}
  bidResults: ${(db.bidResults || []).length}
  bidPackages: ${(db.bidPackages || []).length}
  reports: ${(db.reports || []).length}
  alerts: ${(db.alerts || []).length}
  intelSources: ${intelSourceCount}`);
}

main();
